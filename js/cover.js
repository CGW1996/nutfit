$("#nutCover").mouseenter(function () {
    // $("#nutCoverTitle").css("color", "black");
    $(".nutImg").css({ "opacity": "0.8", "transition": ".8s" })
    $("#nutLine").css({"width":"150px", "transition": ".8s"})
})
$("#nutCover").mouseleave(function () {
    // $("#nutCoverTitle").css("color", "white")
    $(".nutImg").css("opacity", ".5")
    $("#nutLine").css({"width":"300px", "transition": ".8s"})
})
